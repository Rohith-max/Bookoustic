// Get font family for a category
export function getFontForCategory(category: string) {
  return categoryFonts[category as keyof typeof categoryFonts] || categoryFonts["Fiction"]
}

// Get theme for a category
export function getThemeForCategory(category: string) {
  return categoryThemes[category as keyof typeof categoryThemes] || defaultTheme
}

// Generate Google Fonts URL for all fonts
export function getAllFontsUrl() {
  const uniqueFonts = new Set<string>()

  Object.values(categoryFonts).forEach((font) => {
    uniqueFonts.add(`family=${font.title.replace(/ /g, "+")}:wght@${font.titleWeight}`)
    uniqueFonts.add(`family=${font.body.replace(/ /g, "+")}:wght@${font.bodyWeight}`)
  })

  return `https://fonts.googleapis.com/css2?${Array.from(uniqueFonts).join("&")}&display=swap`
}
